let grade = 5;

switch (grade) {
    case 1:
    case 2:
        console.log("Bad");
        break;
    case 3:
        console.log("OK");
        break;
    case 4:
        console.log("Good");
        break;
    case 5:
        console.log("Great!");
        break;
    default:
        console.log("There's no such a grade!");
}